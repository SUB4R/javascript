/*---------------------------------WINDOW---------------*/

/*

L'objet window est un objet globalqui représente la fenêtre du navigateur
 cet objet est créer automatiquement lorsque la page web est chargée 

*/

/*  

L'objet window est un objet important en javascript car il fournit une
 interface entre le code Javascript et la fenetre du javascript

*/


console.log (window) ; 

// La propriété `window.console` permet d'accéder à l'objet console
console.log(window.console);
// La propriété `window.alert()` permet d'afficher une boite de dialogue
window.alert('hello,world!');

// La propriété `window.document` permet d'accéder à l'objet document
console.log(window.document);